import java.lang.Math;

class Mathematics{
    public int add(int a, int b){
        return a+b;
    }
    public int subtract(int a, int b){
        return a-b;
    }
    public int multiply(int a, int b){
        return a*b;
    }
    public int divide(int a, int b){
        return a/b;
    }
    public double squareRoot(int a){
        return Math.sqrt(a);
    }
    public int modulo(int a, int b){
        return a%b;
    }
}